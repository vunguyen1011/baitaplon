   public void capnhatthongtin(ArrayList<Student> dsSV){
       DefaultTableModel md=(DefaultTableModel) tbStudent.getModel();
       md.setRowCount(0);
       
       for(Student t: dsSV){
        md.addRow(new Object[]{
        t.getStudentId(), t.getName(),t.getGender(), t.getDob(), t.getClassRoon(), t.getAddress(), t.getMajor(),t.getPhone(),t.getEmail(),t.getPassword(),
    });
           
       }
   }
