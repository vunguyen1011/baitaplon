  public void Insert(Student t) {
    dsSV.add(t);

    Connection c = null;
    try {
        c = JDBCUtil.getConnection();
        DAOstudent.getInstance().insert(t);

        SimpleDateFormat displaySdf = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate = displaySdf.format(t.getDob());

        DefaultTableModel md = (DefaultTableModel) tbStudent.getModel();
        md.addRow(new Object[]{
                t.getStudentId(), t.getName(), t.getGender(), formattedDate, t.getClassRoon(),
                t.getAddress(), t.getMajor(), t.getPhone(), t.getEmail(), t.getPassword(),
        });
    } catch (Exception e) {
        e.printStackTrace();
        // Handle or log the SQL exception appropriately
    } finally {
        
        if (c != null) {
            try {
                c.close();
            } catch (Exception e) {
                e.printStackTrace();
                // Handle or log the SQL exception appropriately
            }
        }
    }
}   

